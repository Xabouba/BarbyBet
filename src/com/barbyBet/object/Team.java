package com.barbyBet.object;


public class Team {

	private String _team;
	private String _img;
	
	public Team()
	{
		
	}
	
	public String getTeam() 
	{
		return _team;
	}
	
	public void setTeam(String team) 
	{
		this._team = team;
	}

	public String getImg() 
	{
		return _img;
	}
	
	public void setImg(String img) 
	{
		this._img = img;
	}

}
